    public ChatJanela(String nome, String ip, String porta) throws UnknownHostException {
        
        
        
        
        int intPorta = Integer.parseInt(porta);
        InetAddress in = InetAddress.getByName(ip);
        this.nome = nome;
        
        initComponents();
        
        try {
            s = new Socket(in, intPorta);

        } catch (Exception e) {
            showMessageDialog(null, "NÃ£o se conectou ao servidor");
            System.exit(0);
        }
        
        Thread();
    }
    
    
    private void Thread(){
    
        Thread t = new Thread(new Runnable(){
            
            String mensagem;
            
            @Override
            public void run() {
                
                try {
                    isr = new InputStreamReader(s.getInputStream());
                    br = new BufferedReader(isr);
                    
                    while((mensagem = br.readLine()) != null){
                        
                        msgRecebida.setText(msgRecebida.getText() + mensagem + "\n");
                        
                    }
                } catch (IOException ex) {
                    Logger.getLogger(ChatJanela.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            
        });
        t.start();
        
    }
